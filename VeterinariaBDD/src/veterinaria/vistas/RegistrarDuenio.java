    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed

            if(Utilidad.checharNumeroEntero(txtNumero.getText())){
                dt.setNombre(txtNombre.getText());
                dt.setApellidoPaterno(txtApellidoP.getText());
                dt.setApellidoMaterno(txtApellidoM.getText());
                dt.setDireccionCalle(txtCalle.getText());
                int numeroDireccion = Integer.parseInt(txtNumero.getText());
                dt.setDireccionNumero(numeroDireccion);
                dt.setDireccionColonia(txtColonia.getText());
                dt.setEmail(txtEmail.getText());
                dao.crearDuenio(dt);
                Utilidad.mostrarAlertaSimple(JOptionPane.INFORMATION_MESSAGE, "Exito", "Dueño registrado correctamente");
            } else{
                Utilidad.mostrarAlertaSimple(JOptionPane.ERROR_MESSAGE, "Los datos que diste son invalidos", "Cambia el tipo de dato al esperado (checar Numero de casa)");
            }
            

    }//GEN-LAST:event_btnRegistrarActionPerformed

